    /**
     * @param string $query
     * @return mixed
     */
    public function findPostsByName(string $query){
        $qb = $this->createQueryBuilder('p');
        $qb 
            ->where(
                $qb->expr()->andX(
                    $qb->expr()->orX(
                        $qb->expr()->like('p.title',':query'),
                        $qb->expr()->like('p.description',':query')

                    ),
                )
               
            )
            ->setParameter('query', '%'.$query.'%' );
            return $qb
                ->getQuery()
                ->getResult();
    }
